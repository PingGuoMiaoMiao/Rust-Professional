pub fn find_duplicates(nums: Vec<i32>) -> Vec<i32> {
    let mut nums = nums;
    let mut duplicates = Vec::new();

    for i in 0..nums.len() {
        let index = (nums[i].abs() - 1) as usize;
        if nums[index] < 0 {
            duplicates.push(nums[i].abs());
        } else {
            nums[index] = -nums[index];
        }
    }

    duplicates
}

